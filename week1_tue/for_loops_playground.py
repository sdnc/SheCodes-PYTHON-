# index example of range(0, 1, 2)
# if range has only 1 input, index 0 represent the int range will count up to (exclusive) starting with 0
# if range has several inputs, index 0 represents where range will start to count from (inclusive)
# the second input (index 1) represents where range will count up until (exclusive)
# if there is a third input (index 2), it represents the steps range will count in (e.g. if third input = 2, it will count in steps of two)

for num in range(0, 101):
	print(num)

print("----------------------------------------")

for num in range(0, 101, 5):
	print(num) 
